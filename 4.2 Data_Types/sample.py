#type of data in python

# str
x = str("Hello World")

#int

x = int(20)

#float

x = float(20.5)

#complex 
x = 5j

#List

x = list(("apple","banana","cherry"))

#Tuple

x = tuple(("apple","banana","cherry"))

#range

x = range(6)

#dictionary

x = dict(name="john",age=36)

#set

x = set(("apple","banana","cherry"))

#frozenset

x = frozenset(("apple","banana","cherry"))

#bool

x = bool(5)

#bytes

x = bytes(5)

#bytearray

x = bytearray(5)

#memoryview
x = memoryview(bytes(5))